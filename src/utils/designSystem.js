// Helper function to generate responsive styles
export function responsive(styles) {
  return Object.keys(styles).reduce((acc, breakpoint) => {
    if (breakpoint === 'base') {
      return { ...acc, ...styles[breakpoint] };
    }
    
    const minWidth = breakpoints[breakpoint];
    if (!minWidth) return acc;
    
    acc[`@media (min-width: ${minWidth}px)`] = styles[breakpoint];
    return acc;
  }, {});
}

// Helper function to combine classNames
export function cn(...classes) {
  return classes.filter(Boolean).join(' ');
} 